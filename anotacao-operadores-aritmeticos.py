# Outra forma de realizar a exponenciação pow(2,3). É o mesmo que dizer 2**3, porém a ordem de PRECEDENCIA não é levada em conta
print ((pow(2,3)))

# Para realizar raíz quadrada, 81**(1/2). Calcular a raíz quadrada de alguma coisa é o mesmo que criar a potência dele por (1/2)
print (81**(1/2))

# Segue a mesma lógica para outras raízes
resultado= (81**(1/3))
print('Resultado formatado: {:.2f}'.format (resultado))